package com.company;

public class Calculator {


    public int adunare(int z, int y) {
        int rezultat = z + y;
        return rezultat;
    }

    public int scadere(int z, int y) {
        int rezultat = z - y;
        return rezultat;
    }

    public int inmultire(int z, int y) {
        int rezultat = z * y;
        return rezultat;
    }

    public int impartire(int z, int y) {
        int rezultat = z / y;
        return rezultat;
    }
}
